    private void issueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueButtonActionPerformed
        File f = new File("issuedBooks.txt");
        int id=count+1;
        String callNo=(callNoText.getText());
        int StudentId=Integer.parseInt(studentIdText.getText());
        String StudentName=(studentNameText.getText());
        String StudentContact=(studentContactText.getText());
                     
        try {
            FileWriter fw = new FileWriter("issuedBooks.txt",true);
            PrintWriter pw = new PrintWriter(fw);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date d =new Date(System.currentTimeMillis());
            pw.print("\n" +id +" " +callNo +" " +StudentId +" " +StudentName +" " +StudentContact +" ");
            pw.print(sdf.format(d));
            pw.close();
        } catch (IOException ex) {
            Logger.getLogger(AddLibrarian.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this,"Books issued Successfully!");
                                             

    }//GEN-LAST:event_issueButtonActionPerformed

