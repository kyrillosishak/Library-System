    public void readFile() throws ParseException{
        File f = new File("issuedBooks.txt");
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Scanner s = null;
        try {
            s = new Scanner(f);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(AdminLoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }        
        count=0;
         while(s.hasNext()){
            int id=Integer.parseInt(s.next());
            id=count+1;
            String callNo=s.next();
            int studentId = Integer.parseInt(s.next());
            String studentName=s.next();
            String studentContact = s.next();
            String dateStr = s.next();
            Date d = sdf.parse(dateStr);
            ib[count] = new issuedBooks(id,callNo,studentId,studentName,studentContact);
            ib[count].setAdded_date(d);
            count++;
        }
    }

